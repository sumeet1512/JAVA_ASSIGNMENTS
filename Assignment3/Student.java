public class Student {
    private String name;
    private int prn;
    private String dob;
    public void setName(String name)
    {
        this.name=name;

    }
    public void setPrn (int prn)
    {
        this.prn=prn;
    }
    public void setDob(String Dob)
    {
        this.dob=Dob;
    }
    public String getName()
    {
        return name;
    }
    public int getPrn()
    {
        return prn;
    }
    public String getDob()
    {
        return dob;
    }


}

